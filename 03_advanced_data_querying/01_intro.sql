/* INTRO TO JOINS

- INNER JOIN => Retrieve data from left and right tables where both are having matching records.
- LEFT JOIN => Retrieve all data from the left table and matching records from the right table.
- RIGHT JOIN => Retrieve all data from the right table and matching records from the left table.
- OUTER JOIN => Retrieve all data from both the left and right table irrespective whether they match or not.

WHY JOINS ARE IMPORTANT
- Combining data
- Enhancing analysis
- Data relationships
- Efficiency
- Real-world applications (e.g: generating reports, combining user data with transactions data in e-commerce, insurance etc)
*/